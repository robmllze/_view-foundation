extension ColorSchemeMixesExtension on ColorScheme {
  //
  //
  //

  Color get backdropColorMix {
    return LerpBlender(
      color1: this.background,
      color2: this.shadow,
      blendWeight: 0.75,
    ).blend().withOpacity(0.5);
  }
}
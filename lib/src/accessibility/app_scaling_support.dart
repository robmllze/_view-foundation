/// Scales the app and saves the scale to the device's storage.
void scaleAppBy(double scale) async {
  await updateAppScaleToDeviceStorage((e) => e * scale);
  // Refresh the current theme to apply the new scale.
  final currentTheme =
      appThemes.where((e) => e.key == pAppTheme.value.key).first;
  await pAppTheme.set(currentTheme.builder());
}
const SCREEN_PROPERTIES = {
  "backgroundColor": "Color?",
  "isTranslucent": "bool?",
  "titleColor": "Color?",
  "transitionDuration": "Duration?",
};

@GenerateMakeups(properties: SCREEN_PROPERTIES)
abstract class Screen extends StatefulWidget {
  //
  //
  //

  final ModelScreenConfiguration? configuration;
  final Duration? controllerCacheTimeout;

  //
  //
  //

  const Screen({
    super.key,
    this.configuration,
    this.controllerCacheTimeout = Duration.zero,
  });

  //
  //
  //

  ScreenController createController(
    Screen screen,
    ScreenView state,
  ) {
    return ScreenController(screen, state);
  }
}
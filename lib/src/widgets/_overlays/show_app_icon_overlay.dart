Future<void> showAppIconOverlay(
  BuildContext context, {
  void Function(void Function() remove)? remover,
}) {
  return showOverlay(
    context,
    tapBackgroundToDismiss: false,
    builder: (context, remove) {
      remover?.call(remove);
      return const Center(
        child: MyAppIconProgressIndicator(),
      );
    },
  );
}
